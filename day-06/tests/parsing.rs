use day_06::Map;

#[test]
fn parse_map() {
    let input = "\
....#.....
.........#
..........
..#.......
.......#..
..........
.#..^.....
........#.
#.........
......#...";
    let map = Map::new(input);
    assert_eq!(41, map.visited());
}
#[test]
fn paradoxes() {
    let input = "\
....#.....
.........#
..........
..#.......
.......#..
..........
.#..^.....
........#.
#.........
......#...";
    let map = Map::new(input);
    assert_eq!(6, map.paradoxes());
}